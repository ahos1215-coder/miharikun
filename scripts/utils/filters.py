def is_noise(title: str, summary: str) -> tuple[bool, str]:
    """
    タイトルと要約からノイズかどうかを判定。
    Returns: (is_noise: bool, reason: str)
    """
    combined = f"{title} {summary}".lower()

    # ★ ホワイトリスト: これらを含む記事は絶対に KEEP
    for kw in WHITELIST_KEYWORDS:
        if kw.lower() in combined:
            return False, ""

    # 要約なし判定
    if EMPTY_SUMMARY_IS_NOISE:
        summary_stripped = (summary or "").strip()
        if not summary_stripped or summary_stripped == "(要約なし)":
            if not any(kw.lower() in title.lower() for kw in KEEP_KEYWORDS):
                return True, "要約なし（未分類）"

    # 単独キーワード除外
    for kw in NOISE_KEYWORDS_SINGLE:
        if kw.lower() in combined:
            return True, f"単独: {kw}"

    # AND条件除外
    for kw1, kw2 in NOISE_KEYWORD_PAIRS:
        if kw1.lower() in combined and kw2.lower() in combined:
            return True, f"AND: {kw1}+{kw2}"

    return False, ""


def should_exclude_rss(title: str, summary: str) -> bool:
    """RSS入口用の除外チェック。is_noise のラッパー。"""
    noise, _ = is_noise(title, summary)
    return noise
